  <section>
    <!-- <h2>List of Collective Nouns</h2> -->
    <dl>
      <dt>Vanity</dt>
      <dd>A <strong>vanity</strong> is ten(10) or more domains owned by a single person, where very few are in use.</dd>
      <dt>Arsenal</dt>
      <dd>An <strong>arsenal</strong> is a stackoverflow question with 6 or more popular answers.</dd>
      <dt>Parade</dt>
      <dd>A <strong>parade</strong> of RSS feeds is when you have so many RSS feeds that you have to scroll to see them all.</dd>
      <dt>Quabble</dt>
      <dd>A <strong>quabble</strong> of Tweets is the responses or quote tweets that spawn from an especially hot/unpleasant take.</dd>
      <dt>Megabyte</dt>
      <dd>A <strong>megabyte</strong> of JavaScript developers refers to enough developers to drive a consenus that 1 megabyte of JavaScript is entirely reasonable.</dd>
      <dt>Rendering</dt>
      <dd>A <strong>rendering</strong> of Single-Page Applications is three or more distinct SPAs mashed together to appear as one to the end user.</dd>
    </dl>
