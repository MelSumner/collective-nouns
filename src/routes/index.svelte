    <p class="intro">Collective nouns are names for a collection or a number of people or things. While there are many <a href="https://www.gingersoftware.com/content/grammar-rules/nouns/collective-nouns/" rel="external">examples of collective nouns available online</a>, there are not any well-known resources for collective nouns related to the web.</p>
